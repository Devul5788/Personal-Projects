package com.devul.GPAMapper.app.Calculations;

public class SubjectPercentCount {
    int percent, count;

    public SubjectPercentCount() {
    }

    public SubjectPercentCount(int percent, int count) {
        this.percent = percent;
        this.count = count;
    }

    public int getPercent() {
        return percent;
    }

    public void setPercent(int percent) {
        this.percent = percent;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
